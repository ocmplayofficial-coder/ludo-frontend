export const getTokenCoords = (token, color, index) => {
  if (!token) return { x: 0, y: 0 };

  if (token.status === "home" || token.position === -1) {
    const baseKey = `${color}_base`;
    return POSITION_MAP[baseKey]?.[index] || { x: 15, y: 15 };
  }

  if (token.status === "finished") {
    return { x: 50.3, y: 50 };
  }

  if (token.position >= 100) {
    const homeKey = `${color}_home`;
    return POSITION_MAP[homeKey]?.[token.position] || { x: 50.3, y: 50 };
  }

  return POSITION_MAP.path[token.position] || POSITION_MAP.path[1];
};
